"""Robust ablation metrics for stop-after-layer KG evaluation.

These metrics are intended for ablation settings where a relaxed/domain-guided
relation evaluator can recover many gold relations from very large generated
outputs. In that situation, relaxed relation F1 alone can be misleading because
false positives may be hidden by canonicalization. The metrics here explicitly
measure the trade-off between gold coverage and output efficiency.
"""

from __future__ import annotations

from dataclasses import asdict, dataclass
from math import isfinite
from typing import Any, Iterable


Number = int | float | None


def safe_div(numerator: Number, denominator: Number, default: float = 0.0) -> float:
    """Safely divide two numbers and return ``default`` for invalid denominators."""
    try:
        n = float(numerator or 0.0)
        d = float(denominator or 0.0)
    except (TypeError, ValueError):
        return default

    if d == 0.0:
        return default

    return n / d


def harmonic_mean(values: Iterable[Number], *, default: float = 0.0) -> float:
    """Harmonic mean for positive values only.

    Any missing, zero, negative, NaN, or infinite value makes the harmonic mean
    equal to ``default``. This is deliberate: the score should be zero if one of
    the required quality dimensions collapses.
    """
    vals: list[float] = []
    for value in values:
        try:
            x = float(value or 0.0)
        except (TypeError, ValueError):
            return default
        if x <= 0.0 or not isfinite(x):
            return default
        vals.append(x)

    if not vals:
        return default

    return len(vals) / sum(1.0 / x for x in vals)


@dataclass(slots=True)
class RobustAblationMetrics:
    """Metrics for comparing stop-after-layer outputs.

    Definitions
    -----------
    gold_relation_coverage:
        matched gold relations / gold relations. This is equivalent to relaxed
        relation recall when relation_tp comes from the domain evaluator.

    raw_output_efficiency:
        matched gold relations / raw generated triples. This penalizes outputs
        that recover gold relations only by producing many triples.

    coverage_efficiency_f1:
        Harmonic mean of gold_relation_coverage and raw_output_efficiency.
        This is the recommended main ablation score for prefix-finalized outputs.

    entity_relation_balanced_f1:
        Harmonic mean of coverage_efficiency_f1 and entity_f1. This is stricter
        and useful when entity normalization quality matters for the final KG.
    """

    matched_gold_relations: int
    gold_relation_count: int
    raw_triple_count: int
    canonical_pred_relation_count: int | None

    gold_relation_coverage: float
    raw_output_efficiency: float
    canonical_output_efficiency: float | None
    coverage_efficiency_f1: float
    entity_relation_balanced_f1: float | None

    overgeneration_factor: float | None
    excess_triples_per_matched_relation: float | None
    raw_noise_pressure: float
    gold_miss_rate: float
    triples_per_gold_relation: float
    matched_relations_per_100_triples: float

    def to_dict(self) -> dict[str, Any]:
        return asdict(self)


def compute_robust_ablation_metrics(
    *,
    relation_tp: int | float,
    gold_relations: int | float,
    raw_triples: int | float,
    canonical_pred_relations: int | float | None = None,
    entity_f1: float | None = None,
) -> dict[str, Any]:
    """Compute robust ablation metrics from an evaluated output.

    Parameters
    ----------
    relation_tp:
        Number of gold relations matched by the evaluator.
    gold_relations:
        Number of gold relations.
    raw_triples:
        Number of triples emitted before domain/GT-guided canonicalization.
        For stop-after-layer LLM outputs, this should be the size of the raw
        ``triples.json`` generated by the finalizer, not just the canonicalized
        relation count seen by the evaluator.
    canonical_pred_relations:
        Optional number of canonicalized prediction triples seen by the evaluator.
    entity_f1:
        Optional entity F1 from the same evaluation run.

    Returns
    -------
    dict
        JSON-serializable metrics.
    """
    tp_i = int(relation_tp or 0)
    gold_i = int(gold_relations or 0)
    raw_i = int(raw_triples or 0)
    canonical_i = None if canonical_pred_relations is None else int(canonical_pred_relations or 0)

    coverage = safe_div(tp_i, gold_i)
    raw_efficiency = safe_div(tp_i, raw_i)
    canonical_efficiency = None if canonical_i is None else safe_div(tp_i, canonical_i)
    coverage_efficiency = harmonic_mean([coverage, raw_efficiency])

    entity_relation_balanced = None
    if entity_f1 is not None:
        entity_relation_balanced = harmonic_mean([coverage_efficiency, entity_f1])

    overgeneration_factor = None
    excess_per_match = None
    if tp_i > 0:
        overgeneration_factor = safe_div(raw_i, tp_i)
        excess_per_match = safe_div(max(raw_i - tp_i, 0), tp_i)

    metrics = RobustAblationMetrics(
        matched_gold_relations=tp_i,
        gold_relation_count=gold_i,
        raw_triple_count=raw_i,
        canonical_pred_relation_count=canonical_i,
        gold_relation_coverage=coverage,
        raw_output_efficiency=raw_efficiency,
        canonical_output_efficiency=canonical_efficiency,
        coverage_efficiency_f1=coverage_efficiency,
        entity_relation_balanced_f1=entity_relation_balanced,
        overgeneration_factor=overgeneration_factor,
        excess_triples_per_matched_relation=excess_per_match,
        raw_noise_pressure=1.0 - raw_efficiency if raw_i else 0.0,
        gold_miss_rate=1.0 - coverage if gold_i else 0.0,
        triples_per_gold_relation=safe_div(raw_i, gold_i),
        matched_relations_per_100_triples=100.0 * raw_efficiency,
    )
    return metrics.to_dict()


def add_robust_ablation_metrics_to_summary(
    summary: dict[str, Any],
    *,
    raw_triple_count: int | float | None = None,
) -> dict[str, Any]:
    """Add robust ablation metrics to an evaluation summary in-place.

    The function accepts summaries produced by ``evaluate_domain_kg``. The raw
    triple count should come from the pre-canonicalized output folder. If it is
    omitted, the function falls back to ``pred_relations_count``.
    """
    relation = summary.get("relation", {}) or {}
    entity = summary.get("entity", {}) or {}

    raw_count = raw_triple_count
    if raw_count is None:
        raw_count = summary.get("raw_triples_count")
    if raw_count is None:
        raw_count = summary.get("pred_relations_count", 0)

    robust = compute_robust_ablation_metrics(
        relation_tp=relation.get("tp", 0),
        gold_relations=summary.get("gt_relations_count", relation.get("tp", 0) + relation.get("fn", 0)),
        raw_triples=raw_count,
        canonical_pred_relations=summary.get("pred_relations_count"),
        entity_f1=entity.get("f1"),
    )
    summary["robust_ablation"] = robust
    return summary


def add_robust_ablation_metrics_to_frame(
    df,
    *,
    relation_tp_col: str = "relation_tp",
    gold_relations_col: str = "gold_relations_seen_by_evaluator",
    raw_triples_col: str = "triple_count",
    canonical_pred_relations_col: str = "pred_relations_seen_by_evaluator",
    entity_f1_col: str = "entity_f1",
):
    """Return a copy of a pandas DataFrame with robust ablation columns added."""
    out = df.copy()
    rows = []
    for _, row in out.iterrows():
        metrics = compute_robust_ablation_metrics(
            relation_tp=row.get(relation_tp_col, 0),
            gold_relations=row.get(gold_relations_col, 0),
            raw_triples=row.get(raw_triples_col, 0),
            canonical_pred_relations=row.get(canonical_pred_relations_col, None),
            entity_f1=row.get(entity_f1_col, None),
        )
        rows.append(metrics)

    # Pandas is imported lazily so this module stays lightweight for normal use.
    import pandas as pd  # type: ignore

    metrics_df = pd.DataFrame(rows, index=out.index)
    for col in metrics_df.columns:
        out[col] = metrics_df[col]
    return out
